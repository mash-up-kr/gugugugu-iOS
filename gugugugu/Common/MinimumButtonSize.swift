import UIKit


extension CGFloat {
    
    static let minimumButtonWidth: CGFloat = 44.0
    static let minimumButtonHeight: CGFloat = 44.0
    
}


extension CGSize {

    static let minimumButtonSize = CGSize(width: .minimumButtonWidth, height: .minimumButtonHeight)
    
}
